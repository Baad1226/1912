<h2>Klubkártya</h2>
<br>
<h3>Klubkártya hosszabbítás</h3>
<br>
<p class="entry">A 2015-ben kiadott Előre Klubkártyák érvényessége 2018-ban lejár.</p>
<img class="klubkartya" src="./kepek/elore_klubkartya.jpg" title="Klubkártya" alt="Klubkártya">
<br>
<p class="entry">Klubunk a 2015/2016-os elsőosztályú bajnoki szezon előtt kezdte meg az Előre Klubkártyák kibocsátását és a mai napig már 3.899 drukker élt ezzel a lehetőséggel. A szurkolói kártyák kiadása-kiállítása természetesen továbbra is folyamatos, hiszen használatával a legtöbb Magyarországon megrendezett bajnoki- és/vagy válogatott mérkőzésre belépőt lehet váltani, és az Előre bérletek vásárlásához is elengedhetetlen. Az Előre Klubkártya felmutatásával ráadásul néhány partnerünk szolgáltatásait is kedvezményesen lehet igénybe venni.</p>
<br>
<p class="entry">Aki továbbra is élni szeretne a klubkártya nyújtotta előnyökkel, jövőre mindenképpen ellenőrizze kártyája lejárati idejét! Az elsők között kiállított klubkártyák is érvényesek maradnak a 2017/2018-as szezon végéig, hiszen ezeket legkorábban 2015. májusában adtuk ki. A 2018-as évben lejáró klubkártyák hosszabbítása személyesen lehetséges és mivel ezek már 18 éves kor alatt 5 évig, 18 éves kor felett pedig 10 évig lesznek érvényesek, így kiváltásuk 1.000,- forintos egyszeri költség megfizetésével lehetséges, csakúgy mint a 2017. október 1-je után igényelt teljesen új kártyák esetén. Természetesen online módon továbbra is el lehet indítani az igénylést a bekklubkartya.mlsz.hu oldalon és az átvétel – a szokásos módon – személyesen lehetséges.</p>
<br>
<h3>Klubkártya Kisokos</h3>
<br>
<p class="entry"><span class="kiemelt">Mi a célja a klubkártyának, mikortól kötelező a használata?</span>
<br>
A célja a jegy és bérletvásárlás folyamatának egyszerűsítése. A kártya vonalkódjának leolvasásával a pénztárosnak nem kell bediktálni a szükséges adatokat, a rendszer beolvassa azokat.</p>

<p class="entry"><span class="kiemelt">Mennyibe kerül, milyen okmányokra van szükség a kiváltáshoz?</span>
<br>
Az első Előre Klubkártya kiváltása ingyenes, személyi igazolvány szükséges hozzá.</p>

<p class="entry"><span class="kiemelt">Kinek érdemes kiváltani a klubkártyát?</span>
<br>
Mindenkinek, aki jegyet szeretne vásárolni a mérkőzésekre.</p>

<p class="entry"><span class="kiemelt">Gyermekeknek is ki lehet váltani?</span>
<br>
Igen, 6 éves kortól. Viszont 15 év alattiak esetében a gyermek törvényes képviselőjének (szülő/gondviselő) is jelen kell lenni és igazolni a gyerek személyazonosságát.</p>

<p class="entry"><span class="kiemelt">Mi a helyzet a bérletesekkel, nekik kötelező?</span>
<br>
Igen, a 2017/2018-as NB II-es bajnoki szezonban a bérletvásárláshoz továbbra is szükséges a szurkolói kártya előzetes kiváltása.</p>

<p class="entry"><span class="kiemelt">A VIP-vendégeknek is szükséges kiváltani?</span>
<br>
A szurkolói kártyára a VIP-vendégeknek is szüksége van, a rendelkezés alól csak azok mentesülnek, akiknek belépőjegyet sem kell váltani:
<br>
5 év alatti gyermekek
<br>
akik a stadionban dolgoznak (akkreditált sajtómunkatársak, személyzet, stb.)
<br>
Kivételt a külföldi állampolgársággal rendelkező nézők jelentenek, ők útlevelük, személyi igazolványuk felmutatásával vásárolhatnak belépőjegyet (csak személyesen, számukra nem lehetséges az online jegyvásárlás). Azok a szurkolók, akik csak kerekesszékkel tudnak közlekedni, előzetes regisztráció után kísérőjükkel ingyen tekinthetik meg a Békéscsaba 1912 Előre hazai mérkőzéseit. A igénylést legkésőbb a találkozót megelőző munkanap déli 12 óráig a belepojegy(at)1912elore.hu címen várjuk.</p>

<p class="entry"><span class="kiemelt">Mikor és hol tudom kiváltani?</span>
<br>
A Békéscsaba 1912 Előre Futball Zrt. székházában (5600 Békéscsaba, Kórház u. 6. I. emelet) az aktuális nyitvatartási rend szerint biztosítjuk a kiváltás lehetőségét. A pontos időpontokról a honlapunkon (elorekartya.hu), valamint Facebook oldalunkon (facebook.com/1912elore) folyamatosan tájékoztatást adunk.</p>

<p class="entry"><span class="kiemelt">Online módon van lehetőség a kiváltásra?</span>
<br>
A kiváltás úgynevezett előregisztrációval kezdeményezhető a https://bekklubkartya.mlsz.hu/ elérhetőség alatt. Az kártya átvétele azonban csak személyesen lehetséges.</p>

<p class="entry"><span class="kiemelt">Mi a klubkártya PIN-kód, mikor kell használni?</span>
<br>
A kártyához egy PIN-kód tartozik, ami alap esetben a születési hónapból és napból tevődik össze. Például, ha a szurkoló január 1-én született, akkor a PIN-kódja: 0101, ha december 31-én, akkor: 1231. Az online jegyvásárláskor (meccsjegy.mlsz.hu) elegendő megadni a kártya számát és a kódot. A klubkartya.mlsz.hu oldalon a PIN-kód megváltoztatható.</p>

<p class="entry"><span class="kiemelt">Meddig érvényes a kártya?</span>
<br>
Az érvényességi idő a kiállítástól számított 3 év, 2018. október 1. után 10 év.</p>

<p class="entry"><span class="kiemelt">Hova vehetek jegyet a klubkártyával?</span>
<br>
A Békéscsaba 1912 Előre hazai mérkőzésein bármelyik hazai szektorba, idegenbeli mérkőzéseinken csak a kijelölt vendég szektorba.</p>

<p class="entry"><span class="kiemelt">Saját csapatom mérkőzésein kívül más meccseken is használható?</span>
<br>
Igen, valamennyi bajnoki, Magyar Kupa és válogatott mérkőzésen használható. A rendező klub döntése, hogy más csapat szurkolói kártyájával melyik szektorokba engedi a jegyvásárlást. Ilyen esetben érdemes a mérkőzés előtti napokban figyelni a rendező klub honlapját.</p>

<p class="entry"><span class="kiemelt">Hány kártyám lehet?</span>
<br>
Egy, illetve egy Futballkártya. Ha a szurkoló egy másik csapat kártyáját szeretné kiváltani, akkor meg kell szüntetnie előtte a jelenlegit. Az ilyen irányú igényt írásban kell jelezni a kártyát kiállító klubnak.</p>

<p class="entry"><span class="kiemelt">Mi a Futballkártya?</span>
<br>
A klubkártya egy speciális fajtája az úgynevezett Futballkártya, amelyet az MLSZ bocsát ki azoknak, akik nem kötődnek egyetlen klubhoz, illetve leginkább válogatott mérkőzésekre látogatnak ki. A Futballkártyával az összes olyan mérkőzés látogatható, amelyekre a klubkártya kötelező használata vonatkozik. A kiváltás lehetőségéről az MLSZ honlapján lehet tájékozódni.</p>

<p class="entry"><span class="kiemelt">Mi a teendő, ha a kártya elveszik, ellopják vagy megrongálódik?</span>
<br>
Ezek előfordulását három napon belül jelezni kell a kiállítónak, aki intézkedik a kártya visszavonásáról, illetve tájékoztatást ad az új kártya kiváltásának lehetőségéről.</p>

<p class="entry"><span class="kiemelt">Az MLSZ által kibocsátott Futballkártyához hol lehet hozzájutni?</span>
<br>
Békéscsabán egyelőre még nincs lehetőség Futballkártya kiváltására, legközelebb Debrecenben, Kecskeméten vagy Szegeden, illetve Budapesten (TEX Bálna – 1093 Budapest, Fővám tér 11-12. (Nyitva tartás: H-P 10:00-18:00, Szo 11:00-15:00) lehetséges.
További információ: <a href="http://klubkartya.mlsz.hu" target="_blank">http://klubkartya.mlsz.hu</a></p>

