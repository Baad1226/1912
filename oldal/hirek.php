<p class="entry"><span class="kiemelt">A mérkőzés előtt köszöntötték az Előre színeiben 100. bajnoki mérkőzését játszó vágsellyei születésű 34 éves kapust, Ribánszki Lászlót, aki 2016 óta szolgálja a Békéscsabát.</span></p>
<img class="hirek" src="./kepek/20190331_ribanszki.jpg" title="Ribánszi" alt="Ribánszi">
<p class="entry">Az első percekben tapogatózó játék folyt a pályán, egyik oldalon sem alakult ki helyzet. A 11. percben Viczián bal oldali nagy bedobása után Szélpál pöckölte Mészáros elé a labdát, aki közelről a kapusba lőtt. Rá két percre Király próbálkozott mintegy 25 méterről, de mellé szállt a labda. Meddő mezőnyfölényben játszott a Békéscsaba, de komoly helyzetet nem jegyezhettünk föl.</p>

<p class="entry">A játékrész hajrájához közeledve eltűnt ez a kis különbség is, sőt, a fehér mezben játszó szabolcsiak egyre többet tartózkodtak a békéscsabaiak térfelén. A 35. percben Szalai iramodott meg a jobb oldalon, középre passzolt, de a labda elsuhant a tizenhatoson belül üresen álló Viczián lába előtt. Aztán némiképp váratlanul a 43. percben kis híján vezetést szerzett a Nyíregyháza, Hornyák azonban a tizenhatos vonaláról fölé durrantott. Válaszként a túloldalon ajtó-ablak helyzetben Jovába lőtte a labdát. Összességében az első félidőben küzdelmes, de nem túl izgalmas játékot láthattunk.</p>

<p class="entry">Brusztoltak a csapatok a második játékrészben is, de mivel mindkét oldalon fokozott figyelmet fordítottak a védekezésre, így csak ritkán kerültek helyzetbe a támadók.</p>

<p class="entry">Fél órával a vége előtt tíz főre fogyatkozott a Békéscsaba, Király kiesésével a védekezésé lett a főszerep, bár a hazai együttes nem játszott bunkerfutballt – ha tehette, vezetett kontrákat, de komoly veszélyhelyzetet nem alakított ki a Spartacus kapuja előtt. A szabolcsiak ugyanakkor egyre többször jutottak el a csabaiak kapujáig és egyszer-egyszer Ribánszki tudására is szükség volt, hogy ne szülessen gól. Mégis az Előre került közel a gólhoz, amikor Sinanovic egyéni akciója után kis híján bevette Jova kapuját.</p>

<p class="entry">Végül maradt a döntetlen, ami talán igazságosabbnak tűnik ezúttal, mintha bármelyik csapat három pontot szerzett volna.</p>